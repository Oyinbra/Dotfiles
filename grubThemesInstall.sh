#!/bin/bash

cat << "EOF"
  ____            _       _____ _
 / ___|_ __ _   _| |__   |_   _| |__   ___ _ __ ___   ___  ___
| |  _| '__| | | | '_ \    | | | '_ \ / _ \ '_ ` _ \ / _ \/ __|
| |_| | |  | |_| | |_) |   | | | | | |  __/ | | | | |  __/\__ \
 \____|_|   \__,_|_.__/    |_| |_| |_|\___|_| |_| |_|\___||___/

EOF

# ------------------------------------------------------
# Check and create ~/Tmp directory
# ------------------------------------------------------
TMP_DIR="$HOME/Tmp"
if [ ! -d "$TMP_DIR" ]; then
  mkdir "$TMP_DIR"
fi

# ------------------------------------------------------
# Update grub themes
# ------------------------------------------------------
if [ ! -d "$TMP_DIR/grub2-themes/" ]; then
  git clone "https://github.com/vinceliuice/grub2-themes.git" "$TMP_DIR/grub2-themes/"
fi
cd "$TMP_DIR/grub2-themes/"

# ------------------------------------------------------
# Install grub themes
# ------------------------------------------------------
sudo ./install.sh -b -t tela
